-- Function to get a random question from a specific phase
function getPadawanTrivia(phase)
	if phase == 1 then
		return padawanTrivia_Phase1[getRandomNumber(1, #padawanTrivia_Phase1)]
	elseif phase == 2 then
		return padawanTrivia_Phase2[getRandomNumber(1, #padawanTrivia_Phase2)]
	elseif phase == 3 then
		return padawanTrivia_Phase3[getRandomNumber(1, #padawanTrivia_Phase3)]
	elseif phase == 4 then
		return padawanTrivia_Phase4[getRandomNumber(1, #padawanTrivia_Phase4)]
	elseif phase == 5 then
		return padawanTrivia_Phase5[getRandomNumber(1, #padawanTrivia_Phase5)]
	end
	return nil
end

-- Function to get 3 unique questions from a phase
function getPadawanTriviaQuestions(phase)
	local phaseQuestions

	if phase == 1 then
		phaseQuestions = padawanTrivia_Phase1
	elseif phase == 2 then
		phaseQuestions = padawanTrivia_Phase2
	elseif phase == 3 then
		phaseQuestions = padawanTrivia_Phase3
	elseif phase == 4 then
		phaseQuestions = padawanTrivia_Phase4
	elseif phase == 5 then
		phaseQuestions = padawanTrivia_Phase5
	end

	if phaseQuestions == nil then
		return {}
	end

	-- Return 3 random unique questions
	local usedIndices = {}
	local questions = {}

	for i = 1, 3 do
		local randomIndex
		repeat
			randomIndex = getRandomNumber(1, #phaseQuestions)
		until not usedIndices[randomIndex]

		usedIndices[randomIndex] = true
		table.insert(questions, phaseQuestions[randomIndex])
	end

	return questions
end
